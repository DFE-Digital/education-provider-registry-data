CREATE OR REPLACE VIEW core.current_establishment_local_authority AS
SELECT DISTINCT ON (ea.establishment_id)
    ea.establishment_id,
    ea.establishment_authority_id,
    ea.local_authority_id,
    la.code AS local_authority_code,
    la.name AS local_authority_name,
    ea.relationship_type,
    ea.effective_from,
    ea.effective_to
FROM core.establishment_authority ea
JOIN ref.local_authority la
  ON la.local_authority_id = ea.local_authority_id
WHERE (ea.effective_from IS NULL OR ea.effective_from <= CURRENT_DATE)
  AND (ea.effective_to IS NULL OR ea.effective_to > CURRENT_DATE)
ORDER BY ea.establishment_id, ea.effective_from DESC NULLS LAST,
         ea.establishment_authority_id DESC;

CREATE OR REPLACE VIEW core.establishment_lifecycle_dates AS
SELECT
    e.establishment_id,
    MIN(le.event_date) FILTER (
        WHERE LOWER(le.event_type) IN ('open', 'opened', 'opening')
    ) AS open_date,
    MAX(le.event_date) FILTER (
        WHERE LOWER(le.event_type) IN ('close', 'closed', 'closing')
    ) AS close_date
FROM core.establishment e
LEFT JOIN core.establishment_lifecycle_event le
  ON le.establishment_id = e.establishment_id
GROUP BY e.establishment_id;

