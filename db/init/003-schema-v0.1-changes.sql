-- 4. Local authority reference and effective-dated relationship
-- ------------------------------------------------------------

-- Retain legacy authority_code/authority_name columns during v0.1 migration,
-- but add the normalized FK and dating columns. The relationship is intended
-- to become the source of truth; a current view is provided below.
ALTER TABLE core.establishment_authority
    ADD COLUMN IF NOT EXISTS local_authority_id BIGINT,
    ADD COLUMN IF NOT EXISTS relationship_type TEXT,
    ADD COLUMN IF NOT EXISTS effective_from DATE,
    ADD COLUMN IF NOT EXISTS effective_to DATE,
    ADD COLUMN IF NOT EXISTS recorded_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    ADD COLUMN IF NOT EXISTS source TEXT;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint
        WHERE conname = 'fk_establishment_authority_local_authority'
          AND conrelid = 'core.establishment_authority'::regclass
    ) THEN
        ALTER TABLE core.establishment_authority
            ADD CONSTRAINT fk_establishment_authority_local_authority
            FOREIGN KEY (local_authority_id) REFERENCES ref.local_authority (local_authority_id);
    END IF;

    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint
        WHERE conname = 'chk_establishment_authority_dates'
          AND conrelid = 'core.establishment_authority'::regclass
    ) THEN
        ALTER TABLE core.establishment_authority
            ADD CONSTRAINT chk_establishment_authority_dates
            CHECK (effective_to IS NULL OR effective_from IS NULL OR effective_to > effective_from);
    END IF;
END $$;

CREATE INDEX IF NOT EXISTS idx_establishment_authority_establishment_dates
    ON core.establishment_authority (establishment_id, effective_from, effective_to);

CREATE INDEX IF NOT EXISTS idx_establishment_authority_local_authority
    ON core.establishment_authority (local_authority_id);

-- One open-ended relationship of each type per establishment.
CREATE UNIQUE INDEX IF NOT EXISTS uq_establishment_authority_current_type
    ON core.establishment_authority (establishment_id, COALESCE(relationship_type, 'RESPONSIBLE'))
    WHERE effective_to IS NULL AND local_authority_id IS NOT NULL;

